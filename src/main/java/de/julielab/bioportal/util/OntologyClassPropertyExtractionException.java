package de.julielab.bioportal.util;

public class OntologyClassPropertyExtractionException extends BioPortalOntologyToolsException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8986991610142547683L;

	public OntologyClassPropertyExtractionException() {
		super();
	}

	public OntologyClassPropertyExtractionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public OntologyClassPropertyExtractionException(String message, Throwable cause) {
		super(message, cause);
	}

	public OntologyClassPropertyExtractionException(String message) {
		super(message);
	}

	public OntologyClassPropertyExtractionException(Throwable cause) {
		super(cause);
	}

}
