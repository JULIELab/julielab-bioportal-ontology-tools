package de.julielab.bioportal.util;

public class BioPortalOntologyToolsException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4133135207634998580L;

	public BioPortalOntologyToolsException() {
		super();
	}

	public BioPortalOntologyToolsException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BioPortalOntologyToolsException(String message, Throwable cause) {
		super(message, cause);
	}

	public BioPortalOntologyToolsException(String message) {
		super(message);
	}

	public BioPortalOntologyToolsException(Throwable cause) {
		super(cause);
	}

}
